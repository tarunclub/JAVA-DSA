package com.company;

import java.util.Scanner;

public class primitives {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

//        Primitive Datatype
        int i = 10;
        char ch = 'a';
        boolean bool = true;
        float marks = 0.1f;
        double largeDecimalNumbers = 456764.678;
        long largeIntegerValues = 123456789l;
        /*
        The datatype which cannot be broken further are known as primitive datatype
        eg: int, char, bool etc
         */
    }
}
